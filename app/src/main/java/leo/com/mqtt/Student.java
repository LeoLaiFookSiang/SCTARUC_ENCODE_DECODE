public class Student {
    private String studName;
    private String studProgramme;
    private String studGender;
    private String studYearofStudy;
    private String studPasword;

    public Student() {
    }

    public String getStudName() {
        return studName;
    }

    public void setStudName(String studName) {
        this.studName = studName;
    }

    public String getStudProgramme() {
        return studProgramme;
    }

    public void setStudProgramme(String studProgramme) {
        this.studProgramme = studProgramme;
    }

    public String getStudGender() {
        return studGender;
    }

    public void setStudGender(String studGender) {
        this.studGender = studGender;
    }

    public String getStudYearofStudy() {
        return studYearofStudy;
    }

    public void setStudYearofStudy(String studYearofStudy) {
        this.studYearofStudy = studYearofStudy;
    }

    public String getStudPasword() {
        return studPasword;
    }

    public void setStudPasword(String studPasword) {
        this.studPasword = studPasword;
    }
}
